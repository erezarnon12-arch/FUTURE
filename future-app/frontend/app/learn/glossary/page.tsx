export default function GlossaryPage() {
  const [search, setSearch] = useState("");
  const [openTerm, setOpenTerm] = useState<string | null>(null);

  const filtered = TERMS.map((cat) => ({
    ...cat,
    items: cat.items.filter(
      (item) =>
        item.term.includes(search) ||
        item.definition.includes(search) ||
        item.example.includes(search)
    ),
  })).filter((cat) => cat.items.length > 0);

  const totalTerms = TERMS.reduce((sum, cat) => sum + cat.items.length, 0);

  return (
    <div className="min-h-screen bg-surface">
      {/* Header */}
      <div className="border-b border-white/5 px-8 py-5 text-right">
        <h1 className="text-xl font-bold text-white">מילון מושגים פיננסיים</h1>
        <p className="text-text-secondary text-sm mt-1">{totalTerms} מושגים — הסברים ברורים על כל מה שצריך לדעת</p>
      </div>

      <div className="max-w-5xl mx-auto px-6 py-8">
        {/* Search */}
        <input
          type="text"
          placeholder="חפש מושג..."
          value={search}
          onChange={(e) => setSearch(e.target.value)}
          className="w-full px-4 py-3 rounded-xl bg-surface-2 text-white placeholder-text-muted border border-white/5 focus:border-indigo-500/40 focus:outline-none mb-10 text-right text-base"
        />

        {/* Categories */}
        <div className="flex flex-col gap-10">
          {filtered.map((cat) => (
            <div key={cat.category}>
              {/* Category heading */}
              <div className="flex items-center gap-3 mb-4">
                <h2 className="text-lg font-bold text-white">{cat.category}</h2>
                <div className="flex-1 h-px bg-white/8" />
                <span className="text-xs text-text-muted">{cat.items.length} מושגים</span>
              </div>

              <div className="flex flex-col gap-2">
                {cat.items.map((item) => {
                  const isOpen = openTerm === item.term;
                  return (
                    <div
                      key={item.term}
                      className="rounded-xl border border-white/5 overflow-hidden"
                      style={{ backgroundColor: "var(--surface-2)" }}
                    >
                      <button
                        onClick={() => setOpenTerm(isOpen ? null : item.term)}
                        className="w-full flex items-center justify-between px-5 py-4 text-right hover:bg-white/5 transition-colors"
                      >
                        <span className="font-bold text-white text-base">{item.term}</span>
                        <span className={`text-base font-bold transition-colors ${isOpen ? "text-indigo-400" : "text-text-muted"}`}>
                          {isOpen ? "−" : "+"}
                        </span>
                      </button>
                      {isOpen && (
                        <div className="px-5 pb-5 text-right border-t border-white/5 pt-4">
                          <p className="text-text-secondary text-base leading-relaxed mb-3">
                            {item.definition}
                          </p>
                          <div className="bg-indigo-500/10 border border-indigo-500/20 rounded-lg px-4 py-3 text-right">
                            <span className="text-indigo-400 text-sm font-bold">דוגמה: </span>
                            <span className="text-text-secondary text-base">{item.example}</span>
                          </div>
                        </div>
                      )}
                    </div>
                  );
                })}
              </div>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}
